public class Loops {

    public static void main(String[] args) {

        System.out.println("Her er et for-loop:\n");

        for(int i = 1; i <= 5; i++){
            System.out.println("Dette er d. " + i + ". runde.");
        }

        System.out.println("\nNu til while-loopet:\n");


        int j = 1;
        while(j<=5){
            System.out.println("Dette er d. " + j + ". runde.");
            j++;
        }


    }

}
